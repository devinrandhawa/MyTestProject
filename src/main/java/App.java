public class App {
    public static void main(String []args){
        int myInt = 5;
        double myDouble = -3.5;
        System.out.println( 5 + myInt );
        System.out.println( myDouble + myInt);
        System.out.println( Math.abs(myDouble));//prints absolute value
        System.out.println( Math.pow(myInt, 3));//power
        System.out.println( Math.sqrt(25) );//square root
        System.out.println( Math.min(5, 50) );
        System.out.println( Math.max(5, 50) );
        System.out.println( Math.round(myDouble) );
        System.out.println( Math.random() );

    }
    
}
